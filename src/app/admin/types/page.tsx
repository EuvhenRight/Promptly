'use client'

import {
	AlertDialog,
	AlertDialogAction,
	AlertDialogCancel,
	AlertDialogContent,
	AlertDialogDescription,
	AlertDialogFooter,
	AlertDialogHeader,
	AlertDialogTitle,
} from '@/components/ui/alert-dialog'
import { Button } from '@/components/ui/button'
import {
	Card,
	CardContent,
	CardDescription,
	CardHeader,
	CardTitle,
} from '@/components/ui/card'
import {
	Dialog,
	DialogContent,
	DialogFooter,
	DialogHeader,
	DialogTitle,
} from '@/components/ui/dialog'
import { Input } from '@/components/ui/input'
import { Label } from '@/components/ui/label'
import {
	Table,
	TableBody,
	TableCell,
	TableHead,
	TableHeader,
	TableRow,
} from '@/components/ui/table'
import { useToast } from '@/hooks/use-toast'
import { Loader2, Pencil, PlusCircle, Trash } from 'lucide-react'
import { useEffect, useState } from 'react'

type TypeItem = { id: string; name: string }

export default function AdminTypesPage() {
	const { toast } = useToast()
	const [types, setTypes] = useState<TypeItem[]>([])
	const [loading, setLoading] = useState(true)
	const [newName, setNewName] = useState('')
	const [adding, setAdding] = useState(false)
	const [editType, setEditType] = useState<TypeItem | null>(null)
	const [editName, setEditName] = useState('')
	const [savingEdit, setSavingEdit] = useState(false)
	const [deleteType, setDeleteType] = useState<TypeItem | null>(null)
	const [deleting, setDeleting] = useState(false)

	const fetchTypes = async () => {
		setLoading(true)
		try {
			const res = await fetch('/api/types')
			const data = await res.json()
			setTypes(Array.isArray(data) ? data : [])
		} catch {
			setTypes([])
		} finally {
			setLoading(false)
		}
	}

	useEffect(() => {
		fetchTypes()
	}, [])

	const handleCreate = async () => {
		const name = newName.trim()
		if (!name) {
			toast({
				variant: 'destructive',
				title: 'Name required',
				description: 'Enter a type name.',
			})
			return
		}
		setAdding(true)
		try {
			const res = await fetch('/api/types', {
				method: 'POST',
				headers: { 'Content-Type': 'application/json' },
				body: JSON.stringify({ name }),
			})
			const data = await res.json()
			if (!res.ok) throw new Error(data.error || 'Failed to create')
			toast({ title: 'Type created', description: data.name ?? name })
			setNewName('')
			fetchTypes()
		} catch (e) {
			toast({
				variant: 'destructive',
				title: 'Error',
				description: e instanceof Error ? e.message : 'Failed to create type.',
			})
		} finally {
			setAdding(false)
		}
	}

	const handleUpdate = async () => {
		if (!editType) return
		const name = editName.trim()
		if (!name) {
			toast({ variant: 'destructive', title: 'Name required' })
			return
		}
		setSavingEdit(true)
		try {
			const res = await fetch(`/api/types/${editType.id}`, {
				method: 'PATCH',
				headers: { 'Content-Type': 'application/json' },
				body: JSON.stringify({ name }),
			})
			const data = await res.json()
			if (!res.ok) throw new Error(data.error || 'Failed to update')
			toast({ title: 'Type updated', description: name })
			setEditType(null)
			setEditName('')
			fetchTypes()
		} catch (e) {
			toast({
				variant: 'destructive',
				title: 'Error',
				description: e instanceof Error ? e.message : 'Failed to update type.',
			})
		} finally {
			setSavingEdit(false)
		}
	}

	const handleDelete = async () => {
		if (!deleteType) return
		setDeleting(true)
		try {
			const res = await fetch(`/api/types/${deleteType.id}`, {
				method: 'DELETE',
			})
			const data = await res.json()
			if (!res.ok) throw new Error(data.error || 'Failed to delete')
			toast({ title: 'Type deleted', description: deleteType.name })
			setDeleteType(null)
			fetchTypes()
		} catch (e) {
			toast({
				variant: 'destructive',
				title: 'Error',
				description: e instanceof Error ? e.message : 'Failed to delete type.',
			})
		} finally {
			setDeleting(false)
		}
	}

	return (
		<div className='space-y-6'>
			<div className='flex items-center justify-between gap-4'>
				<h1 className='text-lg font-semibold md:text-2xl'>Types</h1>
			</div>

			<Card>
				<CardHeader>
					<CardTitle>Add type</CardTitle>
					<CardDescription>
						Create a new type. ID is auto-generated by Firestore.
					</CardDescription>
				</CardHeader>
				<CardContent className='flex flex-wrap items-end gap-4'>
					<div className='space-y-2'>
						<Label htmlFor='new-name'>Name</Label>
						<Input
							id='new-name'
							placeholder='e.g. Video'
							value={newName}
							onChange={e => setNewName(e.target.value)}
							disabled={adding}
						/>
					</div>
					<Button onClick={handleCreate} disabled={adding}>
						{adding && <Loader2 className='mr-2 h-4 w-4 animate-spin' />}
						<PlusCircle className='mr-2 h-4 w-4' />
						Add
					</Button>
				</CardContent>
			</Card>

			<Card>
				<CardHeader>
					<CardTitle>All types</CardTitle>
					<CardDescription>
						Edit or delete types. Use POST /api/types (no body) to seed
						defaults.
					</CardDescription>
				</CardHeader>
				<CardContent>
					{loading ? (
						<div className='flex justify-center py-8'>
							<Loader2 className='h-8 w-8 animate-spin text-muted-foreground' />
						</div>
					) : types.length === 0 ? (
						<p className='text-muted-foreground py-4'>
							No types yet. Add one above or seed defaults with: POST /api/types
							(no body).
						</p>
					) : (
						<Table>
							<TableHeader>
								<TableRow>
									<TableHead>ID</TableHead>
									<TableHead>Name</TableHead>
									<TableHead className='w-[120px]'>Actions</TableHead>
								</TableRow>
							</TableHeader>
							<TableBody>
								{types.map(type => (
									<TableRow key={type.id}>
										<TableCell className='font-mono text-sm'>
											{type.id}
										</TableCell>
										<TableCell>{type.name}</TableCell>
										<TableCell>
											<div className='flex gap-2'>
												<Button
													variant='ghost'
													size='icon'
													onClick={() => {
														setEditType(type)
														setEditName(type.name)
													}}
												>
													<Pencil className='h-4 w-4' />
												</Button>
												<Button
													variant='ghost'
													size='icon'
													className='text-destructive hover:text-destructive'
													onClick={() => setDeleteType(type)}
												>
													<Trash className='h-4 w-4' />
												</Button>
											</div>
										</TableCell>
									</TableRow>
								))}
							</TableBody>
						</Table>
					)}
				</CardContent>
			</Card>

			<Dialog
				open={!!editType}
				onOpenChange={open => !open && setEditType(null)}
			>
				<DialogContent>
					<DialogHeader>
						<DialogTitle>Edit type</DialogTitle>
					</DialogHeader>
					<div className='space-y-2 py-4'>
						<Label>Name</Label>
						<Input
							value={editName}
							onChange={e => setEditName(e.target.value)}
							disabled={savingEdit}
							placeholder='Type name'
						/>
						{editType && (
							<p className='text-xs text-muted-foreground'>
								ID: {editType.id} (cannot change)
							</p>
						)}
					</div>
					<DialogFooter>
						<Button
							variant='outline'
							onClick={() => setEditType(null)}
							disabled={savingEdit}
						>
							Cancel
						</Button>
						<Button onClick={handleUpdate} disabled={savingEdit}>
							{savingEdit && <Loader2 className='mr-2 h-4 w-4 animate-spin' />}
							Save
						</Button>
					</DialogFooter>
				</DialogContent>
			</Dialog>

			<AlertDialog
				open={!!deleteType}
				onOpenChange={open => !open && setDeleteType(null)}
			>
				<AlertDialogContent>
					<AlertDialogHeader>
						<AlertDialogTitle>Delete type?</AlertDialogTitle>
						<AlertDialogDescription>
							This will remove &quot;{deleteType?.name}&quot;. Prompts using
							this type will keep the reference but the name may not resolve
							until you re-add a type with the same ID.
						</AlertDialogDescription>
					</AlertDialogHeader>
					<AlertDialogFooter>
						<AlertDialogCancel disabled={deleting}>Cancel</AlertDialogCancel>
						<AlertDialogAction
							onClick={handleDelete}
							disabled={deleting}
							className='bg-destructive text-destructive-foreground hover:bg-destructive/90'
						>
							{deleting && <Loader2 className='mr-2 h-4 w-4 animate-spin' />}
							Delete
						</AlertDialogAction>
					</AlertDialogFooter>
				</AlertDialogContent>
			</AlertDialog>
		</div>
	)
}
